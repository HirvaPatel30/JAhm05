package hirva;

public class FirstClass {

	public static void main(String[] args) 
	{
		
		int a = 35;
		int b = 12;
		int ans;
			
		ans=a+b;
		System.out.println("Addition is: "+a+"+"+b+"="+ans);
			
		ans=a-b;
		System.out.println("Subtraction is: "+a+"-"+b+"="+ans);
			
		ans=a*b;
		System.out.println("Multiplication is: "+a+"*"+b+"="+ans);
		
		ans=a/b;
		System.out.println("Division is: "+a+"/"+b+"="+ans);
			
	}

}
